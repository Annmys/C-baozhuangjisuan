        public 新包装资料? 选择最佳包装(List<新包装资料> 候选包装列表, int 数据条数)
        {
            string 调试信息 = "";
            调试信息 += $"开始选择最佳包装，数据条数: {数据条数}\n";
            调试信息 += $"是否多条灯带: {数据条数 > 1}\n\n";

            // 打印所有候选包装的详细信息
            调试信息 += "候选包装列表：\n";
            foreach (var 包装 in 候选包装列表)
            {
                调试信息 += $"包装名称: {包装.包装名称}\n";
                调试信息 += $"半成品BOM物料码: {包装.半成品BOM物料码}\n";
                调试信息 += $"允许多条包装: {包装.允许多条包装}\n";
                调试信息 += $"是多条专用包装: {包装.是多条短条专用包装}\n";
                调试信息 += $"总有效面积: {包装.总有效面积}\n";
                调试信息 += "-------------------\n";
            }

            新包装资料 选中包装;
            if (数据条数 > 1)
            {
                // 首先过滤出支持多条包装的选项
                var 多条包装列表 = 候选包装列表.Where(x => x.允许多条包装 || x.是多条短条专用包装).ToList();

                if (多条包装列表.Any())
                {
                    // 定义权重分数计算函数
                    int 计算包装分数(新包装资料 包装)
                    {
                        int 分数 = 0;

                        // 600mm包装得50分
                        if (包装.包装名称.Contains("600"))
                        {
                            分数 += 50;
                        }

                        // 多条专用包装得30分
                        if (包装.是多条短条专用包装)
                        {
                            分数 += 30;
                        }

                        // 总有效面积得分（最大20分）
                        double 面积得分 = (包装.总有效面积 / 多条包装列表.Max(x => x.总有效面积)) * 20;
                        分数 += (int)面积得分;

                        return 分数;
                    }

                    // 添加所有包装的得分信息
                    调试信息 += "\n所有候选包装得分：\n";
                    foreach (var 包装 in 多条包装列表.OrderByDescending(x => 计算包装分数(x)))
                    {
                        调试信息 += $"包装名称: {包装.包装名称}\n";
                        调试信息 += $"半成品BOM物料码: {包装.半成品BOM物料码}\n";
                        调试信息 += $"得分: {计算包装分数(包装)}分\n";
                        调试信息 += $"  - 尺寸得分: {(包装.包装名称.Contains("600") ? "50" : "0")}分\n";
                        调试信息 += $"  - 专用包装得分: {(包装.是多条短条专用包装 ? "30" : "0")}分\n";
                        调试信息 += $"  - 面积得分: {(int)((包装.总有效面积 / 多条包装列表.Max(x => x.总有效面积)) * 20)}分\n";
                        调试信息 += "-------------------\n";
                    }

                    // 使用权重分数选择包装
                    选中包装 = 多条包装列表
                        .OrderByDescending(包装 => 计算包装分数(包装))
                        .First();

                    // 显示选中包装的详细信息
                    string 选中信息 = $"最终选择的包装：\n\n" +
                                     $"包装名称: {选中包装.包装名称}\n" +
                                     $"半成品BOM物料码: {选中包装.半成品BOM物料码}\n" +
                                     $"总得分: {计算包装分数(选中包装)}分\n" +
                                     $"允许多条包装: {选中包装.允许多条包装}\n" +
                                     $"是多条专用包装: {选中包装.是多条短条专用包装}\n" +
                                     $"总有效面积: {选中包装.总有效面积}";

                    //MessageBox.Show(选中信息, "选中包装信息", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                else
                {
                    // 如果没有支持多条包装的选项，则使用默认排序
                    选中包装 = 候选包装列表
                        .OrderByDescending(x => x.总有效面积)
                        .FirstOrDefault();
                }
            }
            else
            {
                选中包装 = 候选包装列表
                    .OrderByDescending(x => x.总有效面积)
                    .FirstOrDefault();
            }

            if (选中包装 != null)
            {
                调试信息 += "\n最终选择的包装：\n";
                调试信息 += $"包装名称: {选中包装.包装名称}\n";
                调试信息 += $"半成品BOM物料码: {选中包装.半成品BOM物料码}\n";
                调试信息 += $"允许多条包装: {选中包装.允许多条包装}\n";
                调试信息 += $"是多条专用包装: {选中包装.是多条短条专用包装}\n";
            }

            //MessageBox.Show(调试信息, "包装选择调试信息", MessageBoxButtons.OK, MessageBoxIcon.Information);

            return 选中包装;
        }

    }



